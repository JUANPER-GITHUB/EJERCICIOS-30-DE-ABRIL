x = float(input("Ingrese el primer numero para la suma:"))
y = float(input("Ingrese el segundo numero para la suma:"))
print("La suma de los dos numeros ingresados es:", x + y)
print("La resta de los dos numeros ingresados es:", x - y)
print("La multiplicación de los dos numeros ingresados es:", x * y)
print("La divición de los dos numeros ingresados es:", x / y)
print("El residuo de los dos numeros ingresados es:", x % y)
